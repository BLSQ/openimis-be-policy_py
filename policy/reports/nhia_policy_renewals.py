def calculate_age(dob):
    from core import datetime
    today = datetime.datetime.today()
    return today.year - dob.year - ((today.month, today.day) < (dob.month, dob.day))


def nhia_policy_renewals_query(user, start_date: None, end_date: None, **kwargs):
    # Checking received parameters
    if start_date is None:
        return {"error": "Error - the start date must be selected"}
    if end_date is None:
        return {"error": "Error - the end date must be selected"}
    if start_date > end_date:
        return {"error": "Error - the start date cannot be greater than the end date"}
    # might add an extra parameter here to check if this should display new enrollments only or renewals
    # but there seems to be some issues with renewals
    # let's first split the two reports and maybe later we'll merge them if it makes sense

    header = {
        "start_date": start_date,
        "end_date": end_date,
    }
    report_data = {
        "header": [header]
    }

    # Preparing filters with given parameters and then fetching filtered data
    search_filters = (Q(enrollment_date__range=(start_date, end_date))
                      & Q(validity_to__isnull=True)
                      & Q(policy__validity_to__isnull=True)
                      & Q(policy__stage=Policy.STAGE_RENEWED))
    ips = (InsureePolicy.objects.filter(search_filters)
                                .prefetch_related("insuree")
                                .prefetch_related("insuree__family__location__parent__parent")
                                .prefetch_related("policy__officer")
                                .prefetch_related("policy__product")
                                .order_by("enrollment_date", "expiry_date", "insuree__chf_id"))

    data = []
    for insuree_policy in ips:
        insuree = insuree_policy.insuree
        lga = insuree.family.location.parent.parent
        product = insuree_policy.policy.product
        officer = insuree_policy.policy.officer
        new_data_element = {
            "nin": insuree.chf_id,
            "insuree": f"{insuree.other_names} {insuree.last_name}",
            "lga": f"{lga.code} {lga.name}",
            "gender": insuree.gender.gender,
            "age": calculate_age(insuree.dob),
            "renewal_date": insuree_policy.enrollment_date,
            "expiry_date": insuree_policy.expiry_date,
            "product": f"{product.code} {product.name}",
            "officer": f"{officer.other_names} {officer.last_name}",
        }
        data.append(new_data_element)

    report_data["data"] = data
    return report_data
